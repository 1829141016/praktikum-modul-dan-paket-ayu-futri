import math
def KelilingKubus(s):
    return 12 * s
def VolumeKubus(s):
    return s * s * s
def KelilingBalok(p, l, t):
    return 4 * (p + l + t)
def VolumeBalok(p, l, t):
    return p * l * t
def KelilingAlasTabung(r):
    return 2 * math.pi * r
def VolumeTabung(r, t):
    return 2 * r * r * t

    
